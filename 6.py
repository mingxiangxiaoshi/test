from wordcloud import WordCloud
from scipy.misc import imread
import matplotlib.pyplot as plt
text=open('D:/Jane Eyre.txt','r').read()
bg_pic=imread('D:/11.png')
wc=WordCloud(background_color='white',max_words=200,mask=bg_pic,max_font_size=100,)
wc=wc.generate(text)
plt.imshow(wc)
plt.axis('off')
plt.show()
wc.to_file('word.jpg')
